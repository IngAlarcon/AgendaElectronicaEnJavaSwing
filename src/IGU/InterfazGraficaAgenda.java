    private void btnIzquierdaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIzquierdaActionPerformed
        
        //Guardo el numero que aparece en mi jTextFiel indice 
        indice = Integer.parseInt(textIndice.getText());//Lo convierto de String a int con Integer.parseInt()
        
        indice = indice -1;//al hacer clik en el boton de la izquierda el indice disminuye 

         if (indice >= 0) {//Si se cumple la condicion
                                     
            textIndice.setText(Integer.toString(indice));//Imprime el nuevo indice, pasando el valor de mi indice de int a string
            
            //Imprimo los datos en mis TextFiel segun el indice 
            textDni.setText(dniDatos[indice]);
            textNombre.setText(nombreDatos[indice]);
            textApellido.setText(apellidoDatos[indice]);
            textDireccion.setText(direccionDatos[indice]);
            textTelefono.setText(telefonoDatos[indice]);
            textFechaN.setText(fechaNDatos[indice]);
               
         }
         
    }//GEN-LAST:event_btnIzquierdaActionPerformed

    private void btnDerechaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDerechaActionPerformed
         
         //Guardo el numero que aparece en mi jTextFiel indice
         indice = Integer.parseInt(textIndice.getText());//Lo convierto de String a int con Integer.parseInt()
         indice = indice + 1; // el indice aumenta en 1

        if (indice < 10) {
        
            textIndice.setText(Integer.toString(indice));//Imprime el nuevo indice, pasando el valor de mi indice de int a string 
            
            //Imprimo los datos en mis TextFiel segun el indice 
            textDni.setText(dniDatos[indice]);
            textNombre.setText(nombreDatos[indice]);
            textApellido.setText(apellidoDatos[indice]);
            textDireccion.setText(direccionDatos[indice]);
            textTelefono.setText(telefonoDatos[indice]);
            textFechaN.setText(fechaNDatos[indice]);
        
        }   

    }//GEN-LAST:event_btnDerechaActionPerformed

    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
 
         //Guardo el numero que aparece en mi jTextFiel indice
          indice = Integer.parseInt(textIndice.getText());//Lo convierto de String a int con Integer.parseInt()
         
        //Capturo en varibles mis datos de los TextFiel 
         String dni = textDni.getText();
         String nombre = textNombre.getText();
         String apellido = textApellido.getText();
         String direccion = textDireccion.getText();
         String telefono = textTelefono.getText();
         String fechaN = textFechaN.getText();

         if (indice >= 0 && indice < 10) {//Si se cumple la condicion me permite guarda los datos 
                
               //guardo mis datos en los vectores segun el indice capturado en el TextFiel indice 
                dniDatos [indice] = dni;
                nombreDatos [indice] = nombre ;
                apellidoDatos [indice] = apellido ;
                direccionDatos [indice] = direccion;
                telefonoDatos [indice] = telefono;
                fechaNDatos [indice] = fechaN;
        }
              
    }//GEN-LAST:event_btnGrabarActionPerformed

